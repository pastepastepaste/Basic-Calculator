using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalculatorPrivateAssembly
{
    public class BasicComputation
    {
        public static float addResult(float a, float b)
        {
            return a + b;
        }
        public static float subResult(float a, float b)
        {
            return a - b;
        }

        public static float multiResult(float a, float b)
        {
            return a * b;
        }

        public static float divResult(float a, float b)
        {
            return a / b;
        }
    }
}
